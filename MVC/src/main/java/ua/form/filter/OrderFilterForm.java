package ua.form.filter;

public class OrderFilterForm {
private String date;
private String nameSearch;
private String lastNameSearch;
private String phoneSearch;
private String citySearch;
private String carrierSearch;
private String producerSearch;
private String productTypeSearch;
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
public String getNameSearch() {
	return nameSearch;
}
public void setNameSearch(String nameSearch) {
	this.nameSearch = nameSearch;
}
public String getLastNameSearch() {
	return lastNameSearch;
}
public void setLastNameSearch(String lastNameSearch) {
	this.lastNameSearch = lastNameSearch;
}
public String getPhoneSearch() {
	return phoneSearch;
}
public void setPhoneSearch(String phoneSearch) {
	this.phoneSearch = phoneSearch;
}
public String getCitySearch() {
	return citySearch;
}
public void setCitySearch(String citySearch) {
	this.citySearch = citySearch;
}
public String getCarrierSearch() {
	return carrierSearch;
}
public void setCarrierSearch(String carrierSearch) {
	this.carrierSearch = carrierSearch;
}
public String getProducerSearch() {
	return producerSearch;
}
public void setProducerSearch(String producerSearch) {
	this.producerSearch = producerSearch;
}
public String getProductTypeSearch() {
	return productTypeSearch;
}
public void setProductTypeSearch(String productTypeSearch) {
	this.productTypeSearch = productTypeSearch;
}


}
